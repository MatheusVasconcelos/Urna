package Entidades;

public class Voto {
	private int num;
	private String branco;
	private String nulo;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getBranco() {
		return branco;
	}
	public void setBranco(String branco) {
		this.branco = branco;
	}
	public String getNulo() {
		return nulo;
	}
	public void setNulo(String nulo) {
		this.nulo = nulo;
	}
	
	
}
